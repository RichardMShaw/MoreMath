const modulo = (n1, n2) => {
  return n1 % n2
}

const divide = (n1, n2) => {
  return n1 / n2
}

const multiply = (n1, n2) => {
  return n1 * n2;
}

const subtract = (n1, n2) => {
  return n1 - n2;
}

const addition = (n1, n2) => {
  return n1 + n2;
}

